export function t(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const base = translations[lang][key] ?? key;
  if (!vars) return base;
  return Object.entries(vars).reduce(
    (out, [varName, value]) => out.replace(new RegExp(`{{${varName}}}`, "g"), String(value)),
    base
  );
}
